
public class switchgrade {

	public static void main(String args[])
	{
		int grade = 90;
		int num = 10;
		
		if(grade == 100)
		{
		
	
		switch(grade% 10)
		{
		case 0: System.out.println("A+++");
		break;
		}}
		
		else if(grade>=90)
		{
			switch(grade% num)
			{
			case 0: System.out.println("A");
			break;
			case 2: System.out.println("A");
			}
			num --;
		}
		
		
		
	}
	
}
